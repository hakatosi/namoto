    /**
     * documentController constructor.
     */
    public function __construct()
    {
        $this->modelClass = document::class;
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(documentStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $document = new document;

        $regChat = ChatService::registerInChat($document->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($document->getTable(), $data);

        $document->fill($data);

        $res = $document->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$document], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $document = document::find($id);

        if(! $document) {
            return response()->json(trans("responses.not_found.document"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'document')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $document = $document->load($relations);
        }

        $document = $this->getRemoteData($model, $document, true);

        return response()->json($document, 200, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(documentUpdateRequest $request, $id)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $document = document::find($id);

        if(! $document) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.document")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $data = $this->hashPasswordAttributeIfExists($document->getTable(), $data);

        $result = $document->update($data);

        if($result){
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $document = document::find($id);

        if(! $document) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.document")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $document->delete();

        if($result) {
            return response()->json(['success'=>true, 'message' => trans("responses.delete.document")], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("deleteerror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update column of resource from storage.
     *
     * @param ApiRequest $request
     * @param $id
     * @param $column
     * @return \Illuminate\Http\JsonResponse
     */
    public function updateColumn(ApiRequest $request, $id, $column)
    {
        $document = document::find($id);

        if(! $document) {
            return response()->json(['success'=>false, 'message' =>"document not found"], 404, [], JSON_UNESCAPED_UNICODE);
        }

        if (! isset($document->$column)) {
            return response()->json(['success'=>false, 'message'=>'Column not exists'], 400, [], JSON_UNESCAPED_UNICODE);
        }

        $document->$column = $request->column_value;

        if ($document->save()) {
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json('Failed to update', 400, [], JSON_UNESCAPED_UNICODE);
    }

    public function getIndexedColumnsValueWithCount($column)
    {
        $documents = document::selectRaw("$column as value, COUNT($column) as count")
                            ->havingRaw("COUNT($column)")
                            ->groupBy("$column")
                            ->get();
        return response()->json($documents, 200, [], JSON_UNESCAPED_UNICODE);
    }

