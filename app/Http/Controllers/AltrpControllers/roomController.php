    /**
     * roomController constructor.
     */
    public function __construct()
    {
        $this->modelClass = room::class;
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(roomStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $room = new room;

        $regChat = ChatService::registerInChat($room->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($room->getTable(), $data);

        $room->fill($data);

        $res = $room->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$room], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $room = room::find($id);

        if(! $room) {
            return response()->json(trans("responses.not_found.room"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'room')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $room = $room->load($relations);
        }

        $room = $this->getRemoteData($model, $room, true);

        return response()->json($room, 200, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(roomUpdateRequest $request, $id)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $room = room::find($id);

        if(! $room) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.room")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $data = $this->hashPasswordAttributeIfExists($room->getTable(), $data);

        $result = $room->update($data);

        if($result){
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $room = room::find($id);

        if(! $room) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.room")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $room->delete();

        if($result) {
            return response()->json(['success'=>true, 'message' => trans("responses.delete.room")], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("deleteerror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update column of resource from storage.
     *
     * @param ApiRequest $request
     * @param $id
     * @param $column
     * @return \Illuminate\Http\JsonResponse
     */
    public function updateColumn(ApiRequest $request, $id, $column)
    {
        $room = room::find($id);

        if(! $room) {
            return response()->json(['success'=>false, 'message' =>"room not found"], 404, [], JSON_UNESCAPED_UNICODE);
        }

        if (! isset($room->$column)) {
            return response()->json(['success'=>false, 'message'=>'Column not exists'], 400, [], JSON_UNESCAPED_UNICODE);
        }

        $room->$column = $request->column_value;

        if ($room->save()) {
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json('Failed to update', 400, [], JSON_UNESCAPED_UNICODE);
    }

    public function getIndexedColumnsValueWithCount($column)
    {
        $rooms = room::selectRaw("$column as value, COUNT($column) as count")
                            ->havingRaw("COUNT($column)")
                            ->groupBy("$column")
                            ->get();
        return response()->json($rooms, 200, [], JSON_UNESCAPED_UNICODE);
    }

